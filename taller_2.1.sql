CREATE DATABASE gestion_deportiva;
USE gestion_deportiva;

create table Equipos(
id_equipo INT auto_increment PRIMARY KEY,
nom_Equipo VARCHAR (50) NOT NULL,
ciuda_Equipo VARCHAR (50),
entren_Equipo VARCHAR (100),
estad_Equipo VARCHAR (100)
);

create table Jugadores(
id_jugador INT auto_increment PRIMARY KEY,
nom_Jugador VARCHAR (100) NOT NULL,
fecha_naciJugador DATE,
nacion_Jugador VARCHAR (100),
posici_Jugador VARCHAR (50),
id_equipo INT,
FOREIGN KEY (id_equipo) REFERENCES Equipos(id_equipo)
);

create table Arbitros(
id_arbitro INT auto_increment PRIMARY KEY,
nom_Arbitro VARCHAR (100) NOT NULL,
nacion_Arbitro VARCHAR (100)
);

create table Partidos(
id_partido INT auto_increment PRIMARY KEY,
fecha_Partido DATE NOT NULL,
id_equipo_local INT,
id_equipo_visitante INT,
goles_local INT,
goles_visitante INT,
id_arbitro INT,
FOREIGN KEY (id_equipo_local) REFERENCES Equipos(id_equipo),
FOREIGN KEY (id_equipo_visitante) REFERENCES Equipos(id_equipo),
FOREIGN KEY (id_arbitro) REFERENCES Arbitros(id_arbitro)
);

create table Plantillas(
id_plantilla INT auto_increment PRIMARY KEY,
temporada VARCHAR (20) NOT NULL,
id_equipo INT,
id_jugador INT,
FOREIGN KEY (id_equipo) REFERENCES Equipos(id_equipo),
FOREIGN KEY (id_jugador) REFERENCES Jugadores(id_jugador)
);

create table Torneos(
id_torneo INT auto_increment PRIMARY KEY,
nom_Torneo VARCHAR (100) NOT NULL,
año_Torneo YEAR NOT NULL,
id_equipo_ganador INT,
FOREIGN KEY (id_equipo_ganador) REFERENCES Equipos(id_equipo)
);


